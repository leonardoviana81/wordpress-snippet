<?php
//---------------------------------------------------------------------------------------
// Remove o bug da pesquisa em branco, que ocorre ocasionalmente
//---------------------------------------------------------------------------------------
add_filter( 'request', 'my_request_filter' );
function my_request_filter( $query_vars ) {
    if( isset( $_GET['s'] ) && empty( $_GET['s'] ) ) {
        $query_vars['s'] = " ";
    }
    return $query_vars;
}